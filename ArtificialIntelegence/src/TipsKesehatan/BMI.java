    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

int tinggi =Integer.parseInt(a.getText());
int bb =Integer.parseInt(b.getText());
int bmi;

//height=(tinggi);
//ftheight=(height*height);
bmi=(bb*100*100)/(tinggi*tinggi);


if(bmi<18.5){
    hasil.setText("Berat Badan Kurang");
}
else if(bmi>18.5&&bmi<24.9){
    hasil.setText("Berat Badan Normal");
}
else if(bmi>25&&bmi<29.9){
    hasil.setText("Berat Badan Berlebih");
}
else if(bmi>30&&bmi<39.9){
    hasil.setText("Obesitas");
}
else if(bmi>40){
    hasil.setText("Sangat Obesitas");
}
    }//GEN-LAST:event_jButton1ActionPerformed

